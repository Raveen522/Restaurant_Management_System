    public void load_FoodData(String seleted_category){
                          
        DefaultTableModel item_table_Model = (DefaultTableModel)tblFoodItems.getModel();
        item_table_Model.setRowCount(0);
        
        DefaultTableModel item_Report_table_Model = (DefaultTableModel)tblFoodItemsReport.getModel();
        item_Report_table_Model.setRowCount(0);
        try {
           Class.forName("com.mysql.cj.jdbc.Driver");
            dbConnection = DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurant_management_system_db","root","");
            dbStatement = dbConnection.createStatement();
            
            if(seleted_category=="All" || seleted_category==""){
                query = "SELECT * FROM food_items;";
            }else if(seleted_category!=""){
                query = "SELECT * FROM food_items WHERE Category='"+seleted_category+"'";
            }

            ResultSet db_resultSet = dbStatement.executeQuery(query);
            
            while (db_resultSet.next()){
                String id = String.valueOf(db_resultSet.getInt("ID"));
                String food_name = db_resultSet.getString("Name");
                String category = db_resultSet.getString("Category");
                String price = db_resultSet.getString("Price");
                
                String tbl_Data[]= {id,food_name,category,price};

                item_table_Model.addRow(tbl_Data);
                item_Report_table_Model.addRow(tbl_Data);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CashierScreen.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CashierScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void load_UserData(String selected_Position){
        DefaultTableModel report_item_table_Model = (DefaultTableModel)tblUserTableReport.getModel();
        report_item_table_Model.setRowCount(0);
        
        DefaultTableModel user_table_Model = (DefaultTableModel)tblUserTable.getModel();
        user_table_Model.setRowCount(0);
        
        try {
            if(selected_Position=="All" || selected_Position==""){
                query = "SELECT * FROM users;";
            }else if(selected_Position!=""){
                query = "SELECT * FROM users WHERE position='"+selected_Position+"';";
            }
            
            
            ResultSet db_resultSet = dbStatement.executeQuery(query);
            while (db_resultSet.next()){
                String userID = String.valueOf(db_resultSet.getInt("UserID"));
                String userName = db_resultSet.getString("UserName");
                String position = db_resultSet.getString("Position");
                String password = db_resultSet.getString("Password");
                String address = db_resultSet.getString("Address");
                String telephone = db_resultSet.getString("Telephone");
                String gender = db_resultSet.getString("Gender");
                
                String tbl_Data[] = {userID, userName, position, password, address, telephone, gender };

                user_table_Model.addRow(tbl_Data);
                report_item_table_Model.addRow(tbl_Data);
                
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
    public void load_SalesData(int selected_month){
        double monthlySubtotal=0.0;
        try {
            if(selected_month==0 ){
                query = "SELECT * FROM bill_details;";
            }else if(selected_month!=0){
                query = "SELECT * FROM bill_details WHERE Date LIKE '"+Integer.toString(selected_month+1)+"';";
            }
            
            
            ResultSet db_resultSet = dbStatement.executeQuery(query);
            while (db_resultSet.next()){
                String salesID = String.valueOf(db_resultSet.getInt("ID"));
                String salesDate = db_resultSet.getString("Date");
                String salesTime = db_resultSet.getString("Time");
                String number_Of_Items = String.valueOf(db_resultSet.getInt("Number_Of_Items"));
                String subTotal = String.valueOf(db_resultSet.getDouble("SubTotal"));
                
                monthlySubtotal = monthlySubtotal + db_resultSet.getDouble("SubTotal");
                
                String tbl_Data[] = {salesID, salesDate, salesTime, number_Of_Items, subTotal };

                DefaultTableModel sales_table_Model = (DefaultTableModel) tblSales.getModel();
                sales_table_Model.addRow(tbl_Data);
                
                lblReportSubtotal.setText(Double.toString(monthlySubtotal));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(CashierScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
        
