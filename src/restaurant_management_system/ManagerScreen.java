    private void btnFoodAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFoodAddMouseClicked
        try {
            query = "INSERT INTO food_items values(?,?,?,?)";
            dbPrepareStatement = dbConnection.prepareStatement(query);
            dbPrepareStatement.setInt(1,Integer.parseInt(txtFoodID.getText()));
            dbPrepareStatement.setString(2,txtFoodName.getText());
            dbPrepareStatement.setString(3,cmbCategory.getItemAt(cmbCategory.getSelectedIndex()));
            dbPrepareStatement.setDouble(4,Double.parseDouble(txtFoodPrice.getText()));
            int result_set=dbPrepareStatement.executeUpdate();
            JOptionPane.showMessageDialog(this,"Data added successfully","Inserting info",JOptionPane.INFORMATION_MESSAGE);
            dbPrepareStatement.close();
            
            DefaultTableModel item_table_Model = (DefaultTableModel)tblFoodItems.getModel();
            item_table_Model.setRowCount(0);
            load_FoodData("");
            resetInputs();
            
        } catch (SQLException sQLException) {
            System.out.println(sQLException);
        }
    }//GEN-LAST:event_btnFoodAddMouseClicked

